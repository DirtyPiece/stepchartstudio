    /// The medium difficulty (Trick, Another, Standard, Normal).
    /// </summary>
    Medium,

    /// <summary>
    /// The hard difficulty (Maniac, SSR, Heavy, Crazy).
    /// </summary>
    Hard,

    /// <summary>
    /// The challenge difficulty (SManiac, MAX2 Challenge, EX Challenge).
    /// </summary>
    Challenge,
}
